# Strings

# Python utiliza tipagem dinâmica
# str -> string -> texto

# aspas simples
print('Olá!')
print(1,"'Olá!'") # isso pode

# aspas duplas

print("Hello")
print(1,"'Hello'") # isso pode

# escape

print("Então ele disse: \"Batata\"")

# se quiser que o simbolo de escape apareça

print(r"Então ele disse: \"Batata\"")