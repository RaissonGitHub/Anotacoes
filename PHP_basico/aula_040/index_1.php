/* 
Cookies são um mecanismo para guardar dados do lado do cliente (browser).
Podem ser udados para várias finalidades. Sessões de utilizador é a 
finalidade mais recorrente. Mas podemos guardar outros tipos de informação.

IMPORTANTE: Nunca devemos usar cookies para guardar informação sensível,
uma vez que a leitura dos dados guardados pode ser feita com facilidade.
Nunca devemos esperar que o cookie exista do olado do cliente, porque o mesmo
pode remover os cookies ou alterar os seus valores esperados.

Podemos criar mais do que um cookie por cada site.
*/