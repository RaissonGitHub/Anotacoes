# Operadores aritiméticos

# Adição
adicao = 10 + 10
print('Adição',adicao)

# Subtração
subtracao = 10 - 5
print('Subtração',subtracao)

# Multiplicação
multiplicacao = 2 * 7
print('Multiplicação',multiplicacao)

# Divisão
divisao = 10 / 2  # float
print('Divisão',divisao)

# Divisao inteira
divisao_inteira = 10 // 2 # int ou corta as casas decimais
print('Divisão inteira',divisao_inteira )

# Exponenciação
exponenciacao = 2 ** 3
print('Exponenciação',exponenciacao)

# Módulo | Resto da divisão
modulo = 55 % 2
print('Módulo',modulo)