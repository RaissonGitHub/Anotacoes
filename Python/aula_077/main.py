# Importar módulos em python

# inteiro - import nome_modulo
# vantagens - você tem o namespace do módulo
# desvantagens - você tem nomes muito grandes

# partes - from nome_modulo import objeto1,objeto2
# vantagens - nomes pequenos
# desvantagens: sem o namespace do módulo

# alias 1 - import nome_modulo as apelido
# alias 2 - from nome_modulo import objeto1 as apelido
# vamtagems: voce pode reservar nomes para seu código
# desvantagens: voce pode ficar fora do padrão da linguagem

# má pratica: from nome_modulo import *
# vantagens: importa tudo de um módulo
# desvantagens: importa tudo de um módulo
